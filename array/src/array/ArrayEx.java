package array;

public class ArrayEx {

	public static void main(String[] args) {
		int[] studentID = new int[] {1,2,3,4,5};
		
		for(int i=0; i<5; i++) {
		System.out.println(studentID[i]);
	}
		System.out.println(studentID.length);
		
		for(int i=0; i<studentID.length; i++) {
		System.out.println(studentID[i]);
		}
		
		
}
}
